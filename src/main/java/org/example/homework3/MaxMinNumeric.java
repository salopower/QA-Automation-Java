package org.example.homework3;

public class MaxMinNumeric {
    public static void main(String[] args) {
        int number1 = 10;
        int number2 = 21;

        System.out.println("Minimum: " + Math.min(number1, number2));

        System.out.println("\nMaximum: "+ Math.max(number1, number2));
    }
}